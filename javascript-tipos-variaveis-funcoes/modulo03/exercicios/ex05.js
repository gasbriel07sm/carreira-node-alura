// Crie uma variável com uma string e utilize métodos de manipulação de strings, como toUpperCase, toLowerCase, slice ou outros, para modificar a string original. Exiba os resultados finais no console.

const string = 'Hello world';

console.log('TO UPPER CASE: ' + string.toUpperCase());
console.log('to lowe case: ' + string.toLowerCase());
console.log('slice para pegar hello: ' + string.slice(0, 5)); 
console.log('slice para pegar world: ' + string.slice(6)); // o slice faz o fatiamento da string 
console.log('trim: ' + string.trim());